// Create an arrow function named isPositive that takes one 
// input and returns true if the given number is positive, 
// otherwise returns false.

const isPositive = (number) => number > 0;

// Example
console.log(isPositive(1)); // true
console.log(isPositive(0)); // false
console.log(isPositive(-1)); // false
// console.log(isPositive(0.1)); // true, since 0.1 is greater than 0 in JavaScript
