//Create an arrow function named isLessThan5 that takes one 
//input and returns true if the given number is less than 5, 
//otherwise returns false.

const isLessThan5 = (number) => number < 5;

// Example
console.log(isLessThan5(4)); // true
console.log(isLessThan5(5)); // false
console.log(isLessThan5(6)); // false
// console.log(isLessThan5(4.9)); // true, since 4.9 is less than 5 in JavaScript
