//Create an arrow function named isEqualTo10 that takes one 
//input and returns true if the given number is exactly 10, 
//otherwise returns false.

const isEqualTo10 = (number) => number === 10;

// Example
console.log(isEqualTo10(10)); // true
console.log(isEqualTo10(9)); // false
console.log(isEqualTo10(15)); // false
// console.log(isEqualTo10(10.0)); // true, since 10 and 10.0 are considered equal in JavaScript
