// Create an arrow function named isEven that takes one
// input and returns true if the given number is even,
// otherwise returns false.

const isEven = (number) => number % 2 === 0;

// Example
console.log(isEven(2)); // true
console.log(isEven(3)); // false
console.log(isEven(4)); // true
// console.log(isEven(0)); // true, since 0 is even in JavaScript
